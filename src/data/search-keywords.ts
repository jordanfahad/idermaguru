export const acceptedSearchKeywords = Object.values(acceptedSearchKeywordGroups).flat();

export function expandSearchText(value: string) {
  const additions = keywordAliases
    .filter(([pattern]) => pattern.test(value))
    .map(([, expansion]) => expansion);

  return [value, ...additions].join(" ").trim();
}

export function isAcceptedSearchKeyword(value: string) {
  const lower = value.toLowerCase();
  return acceptedSearchKeywords.some((keyword) => lower.includes(keyword.toLowerCase())) || expandSearchText(value) !== value;
}
